     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
      
        $name = $request->input('name')==null?-1:$request->input('name');
        $codes =  DB::table('register_codes')
                ->join('students','students.id','register_codes.student_id')
                ->where('students.name', '=', $name )
                ->select('students.id','students.name','register_codes.code','register_codes.relationship','register_codes.deleted_at')
                ->get();
        
        return View::make('backend.register_code.index')->with('codes',$codes);
    }

